    private void jLbPainelMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLbPainelMousePressed
        if (evt.isControlDown()) {
            Clipboard clipboard = Toolkit.getDefaultToolkit().getSystemClipboard();
            clipboard.setContents(new StringSelection(jOutputText.getText()), null);
            jTcopiado.setVisible(true);
            jOutputText.setBackground(new Color(155, 90, 90));
        }
    }//GEN-LAST:event_jLbPainelMousePressed

    private void toggleMenu() {
        if (jPainelMenu.isVisible()) {
            if (autoMenu) {
                jLbPainel.setIcon(new ImageIcon(this.getClass().getResource("/mild/katyusha/system/gfx/menu/triforce_auto.png")));
            } else {
                jLbPainel.setIcon(new ImageIcon(this.getClass().getResource("/mild/katyusha/system/gfx/menu/triforce.png")));
            }
            jPainelMenu.setVisible(false);
        } else {
            if (autoMenu) {
                jLbPainel.setIcon(new ImageIcon(this.getClass().getResource("/mild/katyusha/system/gfx/menu/triforce_2_auto.png")));
            } else {
                jLbPainel.setIcon(new ImageIcon(this.getClass().getResource("/mild/katyusha/system/gfx/menu/triforce_2.png")));
            }
            jPainelMenu.setVisible(true);
        }
    }

