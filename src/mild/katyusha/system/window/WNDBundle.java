    private void jInputTextKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jInputTextKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_UP && !(jInputText.getText().contains("Interpretando..."))) {
            jInputText.setText(ultimo);
        }
        if (evt.getKeyCode() == KeyEvent.VK_ENTER && !(jInputText.getText().contains("Interpretando..."))) {
            ultimo = jInputText.getText();
            send();
        }
    }//GEN-LAST:event_jInputTextKeyPressed

    public void send() {
        String comando = jInputText.getText();
        jOutputText.setText("Katyusha : Humm...");
        new Thread(()->{cerebro.enviarComando(comando);}).start();
    }

