    private void jLbPainelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLbPainelMouseClicked
        if (!evt.isControlDown()) {
            if (evt.getButton() == MouseEvent.BUTTON3) {
                if (!jPainelMenu.isVisible()) {
                    if (this.autoMenu) {
                        jLbPainel.setIcon(new ImageIcon(this.getClass().getResource("/mild/katyusha/system/gfx/menu/triforce.png")));
                        this.autoMenu = false;
                    } else {
                        jLbPainel.setIcon(new ImageIcon(this.getClass().getResource("/mild/katyusha/system/gfx/menu/triforce_auto.png")));
                        this.autoMenu = true;
                    }
                }
            } else if (!this.autoMenu) {
                toggleMenu();
            }
        }
    }//GEN-LAST:event_jLbPainelMouseClicked

